#defining functions:
def normalize(series: np.ndarray) -> np.ndarray:
    """
    Ensures the input series has shape (W, T, D) for W walkers, T steps, and D dimensions.
    Prevents errors in corner package when single walker samplers (e.g. HMC) return arrays of (T, D)
    """
    series_arr = np.asarray(series)
    if series_arr.ndim == 2: #check for (T, D) shaped array
        series_arr = series_arr[None, :, :] #reshaping array (T, D) to (W, T, D) w/ dimension = 1
    #now that the array has been reshaped, let's check its size:
    if series_arr.ndim !=3:
        raise ValueError(f'Invalid series shape: should be (T, D) or (W, T, D), instead got {series_arr.shape}')
    return series_arr


def flatten(series: np.ndarray, thin: int = 1) -> np.ndarray:
    """
    Stacks walkers together such that the array is in terms of samples per dimension, or (N, D).
    Needed for use in corner package. Also performs optional thinning for computational efficiency.

    Arguments:
    ----------
    series:
        np.ndarray. An array containing all of the realizations of the particular MCMC sampler upon
        the chosen distribution.
    thin:
        int. The thinning factor, which selects out every thin-th element from the T-th dimension of
        the series array.

    Returns:
    ----------
    flat:
        flattened samples from the input MCMC sampler. has shape (N, D) where N = W * (T/thin)
    """

    s = normalize(series=series) #has shape (W, T, D)
    if thin == None or thin < 1: #repairs thinning factor if an improper value is entered
        thin = 1
    s = s[:, ::thin, :] #performs the thinning
    W, T, D = s.shape #Defines W, T, and D from array shape
    return(s.reshape(W*T, D))

def subsample(samples: np.ndarray, maxp: int | None, seed: int = 0) -> np.ndarray:
    """
    Draws samples at random (without replacement) from the series if said series contains too many points.
    This should improve the efficiency of using the corner library, which uses a lot of memory at high point numbers.
   
    Arguments:
    ----------
    samples:
        The series (possibly flattened/normalized) of sampler datapoints from the particular sampling algorithm
    max:
        the maximum number of points to draw from (suggest 10k-50k points)
    seed:
        RNG seed for random drawing. Should be consistent if comparing different sampling algorithms
    
    Returns:
    ----------
    subsample:
        subsample of walker positions, either of length 'max' or of the original input length (if less than 'max')
    """

    if maxp is None:
        return samples
    
    N = samples.shape[0]
    if N <= maxp:
        return samples
    rng = np.random.default_rng(seed) #generate random number generator
    idx = rng.choice(N, size = maxp, replace = False) #drawing 'max' random indices from 0 to N-1

    subsample = samples[idx] #create a new array of only the randomly selected indices
    return subsample

def group_dims(D, group_size = 10):
    """
    Corner plots get extremely large at high dimensions. In particular:

    # of plots = D(D+1) / 2

    For greater than 10 dimensions, we split it into separate groups.
    """

    groups = [] #initializing empty list for storage
    start = 0 # start at 0th index (i.e. the first variable)

    while start < D: #keep iterating until start >= D
        end = min(start+group_size, D) #select ending point for first grouping. Minimum statement to account for uneven group sizes
        groups.append(list(range(start, end)))
        start += group_size

    return groups

def default_labels(dims: int, prefix: str = 'x') -> list[str]:
    """
    If no labels are fed into the processor (i.e., the variables are unlabeled/arbitrary) generate a
    list of stand-in label names. 
    
    Arguments:
    ----------
    dims:
        number of dimensions of the particular dataset
    prefix:
        arbitrary letter/symbol to represent each dimensional variable. 

    Returns:
    ----------
    labels:
        list of labels for each plot

    """
    return [rf'$x_{{{i}}}$' for i in dims]


def plot_corner(samples: np.ndarray, dims, labels: list[str], title: str, outfile: str, overlay_rosenbrock: dict = None, overlay_gaussian: dict = None, transform:dict = None):
    """
    Generates corner plots for the supplied samples, using the given parameter dimensions.
    Plots are generated using a fed title and list of labels. Plotes are saved at the given directory
    
    Arguments:
    ----------
    samples:
        MCMC samples generated by a sampling algorithm
    dims:
        list of parameter dimensions to make plots of
    labels:
        list of labels for each parameter
    title:
        title for the generated plot
    outfile:
        director path for the generated plot
    """

    samples = transform_helper(samples, transform=transform)
    dim_samples = samples[:, dims] #select samples from the given dimensions
    if labels is None:
        dim_labels = [rf'$x_{{{i}}}$' for i in dims]


    # Remove rows with NaN/Inf in any plotted dimension
    finite_mask = np.all(np.isfinite(dim_samples), axis=1)
    dim_samples = dim_samples[finite_mask]

    if dim_samples.shape[0] < 2:
        print(f"Skipping {title}: not enough finite samples.")
        return

    # Check dynamic range
    spread = np.ptp(dim_samples, axis=0)   # max - min for each plotted dimension
    if np.any(spread < 1e-12):
        bad = np.where(spread < 1e-12)[0]
        print(f"Skipping {title}: no dynamic range in local columns {bad}.")
        return

    fig = corner.corner(dim_samples, #generating corner plots using the corner library
                        bins = 40, 
                        labels = dim_labels, 
                        smooth = 0.9, 
                        show_titles = True, 
                        quantiles = [0.16, 0.5, 0.84], 
                        title_kwargs={'fontsize':10})
    
    if overlay_rosenbrock is not None:

        sigma = overlay_rosenbrock.get('sigma', 0.7)
        a = overlay_rosenbrock.get('a', 1.0)
        b = overlay_rosenbrock.get('b', 100.0)
        overlay_helper_rosenbrock(fig, dim_samples=dim_samples, dims=dims, sigma = sigma, a=a, b=b, color = 'crimson', ngrid = 250)

    elif overlay_gaussian is not None:

        mu = overlay_gaussian.get('mu', None)
        cov = overlay_gaussian.get('cov', None)
        if mu is None or cov is None:
            raise ValueError(f'Improper parameters passed to overlay_gaussian. Mu is {mu}, Cov is {cov} ')
        else:
            overlay_helper_gaussian(fig, dim_samples=dim_samples, dims = dims, mu = mu, cov = cov, color = 'crimson', ngrid = 250)        


    plt.savefig(outfile, dpi = 200, bbox_inches = False)
    plt.close(fig)

def benchmark_corner(results: dict, outdir: str, thin: int = 20, max: int = 30000, groupsize: int = 10, overlay_rosenbrock:dict = None, overlay_gaussian:dict = None, transform:dict = None):
    """
    Wrapper function that uses the results output from the benchmark functions to produce corner plots
    for all of the samplers. 

    Arguments:
    ----------
    results:
        dictionary of results taken from the benchmark examples. Used to draw the series and 
        label information for each sampler where applicable.
    outdir:
        directory to store the finished plots
    thin: 
        thinning factor
    max:
        maximum number of steps to incorporate into the corner plots
    groupsize:
        the number of variables to be plotted at a single time (should be <= 10)
    """


    os.makedirs(outdir, exist_ok = True) #create directory if its not yet made
    
    for name, res in results.items(): #iterate over all of the results in the results dictionary

        
        if "series" not in res: #if, for some reason, the series isn't stored
            print(f'Series not found. skipping {name}')
            continue

        series = res['series'] #define series object
        Nseries = normalize(series) #normalize the series
        Fseries = flatten(Nseries, thin = thin) #flatten series
        Fseries = subsample(Fseries, maxp = max, seed = 0)

        if not np.any(np.all(np.isfinite(Fseries), axis=1)):
            print(f"Skipping {name}: no finite samples.")
            continue
   

        N, D = Fseries.shape
        
        if "labels" not in res:
            print(f'Labels not given for {name}. Generating placeholders...')
            labels = default_labels(D)
        else:
            labels = res['labels']
        
        groups = group_dims(D, group_size=groupsize)

        safe_name = name.replace(' ', '_')

        for gps, dims in enumerate(groups): #iterate over the groups

            outfile = os.path.join(outdir, f'corner_{safe_name}_dims{dims[0]}-{dims[-1]}.pdf') #give an explanatory name
            
            plot_corner(Fseries, dims, labels, f'{name} | dims{dims[0]}-{dims[-1]} | N = {N}', outfile = outfile, overlay_rosenbrock=overlay_rosenbrock, overlay_gaussian=overlay_gaussian, transform = transform) #generate the plot

            print("Saved", outfile)

def rosenbrock_axis_limits(a=1.0, sigma=0.7, b=100.0, nsig=5.0):
    """
    Returns consistent axis limits for Rosenbrock plots.
    """
    xmin = a - nsig * sigma
    xmax = a + nsig * sigma

    noise_std = sigma / np.sqrt(b)

    xmax_abs = max(abs(xmin), abs(xmax))
    ymin = -nsig * noise_std
    ymax = xmax_abs**2 + nsig * noise_std

    return (xmin, xmax), (ymin, ymax)


def rosenbrock_2d_pdf(X, Y, sigma=0.7, a=1.0, b=100.0):
    """
    Exact normalized 2D Rosenbrock pair density:
        p(x, y) = N(x; a, sigma^2) * N(y; x^2, sigma^2 / b)
    """
    prefac = np.sqrt(b) / (2.0 * np.pi * sigma**2)
    expo = -((a - X)**2 + b * (Y - X**2)**2) / (2.0 * sigma**2)
    return prefac * np.exp(expo)


def rosenbrock_x_marginal(xgrid, sigma=0.7, a=1.0):
    """
    Exact marginal for the first coordinate of a Rosenbrock pair.
    x ~ N(a, sigma^2)
    """
    return norm.pdf(xgrid, loc=a, scale=sigma)


def rosenbrock_y_marginal(ygrid, sigma=0.7, a=1.0, b=100.0, nx=4000, x_pad_sigma=6.0):
    """
    Computes the second-coordinate marginal

        p(y) = ∫ N(x; a, sigma^2) N(y; x^2, sigma^2/b) dx

    using dense 1D numerical integration over x.
    """
    noise_std = sigma / np.sqrt(b)

    xmin = a - x_pad_sigma * sigma
    xmax = a + x_pad_sigma * sigma
    xgrid = np.linspace(xmin, xmax, nx)

    # shape (nx, 1)
    px = norm.pdf(xgrid[:, None], loc=a, scale=sigma)

    # shape (nx, len(ygrid))
    py_given_x = norm.pdf(ygrid[None, :], loc=xgrid[:, None]**2, scale=noise_std)

    # integrate over x
    py = np.trapezoid(px * py_given_x, xgrid, axis=0)

    # normalize on y-grid for numerical safety
    if len(ygrid) > 1:
        dy = ygrid[1] - ygrid[0]
        z = np.sum(py) * dy
        if z > 0:
            py /= z

    return py

def overlay_helper_rosenbrock(fig, dim_samples, dims, sigma=0.7, a=1.0, b=100.0,
                              color='crimson', ngrid=400):
    """
    Draw analytic Rosenbrock overlays in the Rosenbrock rest frame.

    Assumes dim_samples have already been inverse-transformed back into the
    rest frame before calling this helper.
    """
    k = len(dims)
    axes = np.array(fig.axes).reshape((k, k))
    local_to_global = {g: i for i, g in enumerate(dims)}

    one_d = {}

    # Build 1D marginals using axis limits, not sample ranges
    for g in dims:
        pair_start = 2 * (g // 2)
        g_first, g_second = pair_start, pair_start + 1

        i = local_to_global[g]
        ax = axes[i, i]
        xmin, xmax = ax.get_xlim()

        grid = np.linspace(xmin, xmax, ngrid)

        if g == g_first:
            pdf = rosenbrock_x_marginal(grid, sigma=sigma, a=a)
        else:
            pdf = rosenbrock_y_marginal(grid, sigma=sigma, a=a, b=b)

        one_d[g] = {"grid": grid, "pdf": pdf}

    # Diagonals
    for g in dims:
        if g not in one_d:
            continue

        i = local_to_global[g]
        ax = axes[i, i]

        grid = one_d[g]["grid"]
        pdf = one_d[g]["pdf"]

        if not np.all(np.isfinite(pdf)) or np.max(pdf) <= 0:
            continue

        ymin, ymax = ax.get_ylim()
        target_height = 0.85 * ymax if ymax > 0 else 1.0
        yplot = pdf / np.max(pdf) * target_height

        ax.plot(grid, yplot, color=color, lw=2.0, alpha=0.8, zorder=10)

    # Lower triangle
    for col_global in dims:
        for row_global in dims:
            if row_global <= col_global:
                continue

            i = local_to_global[col_global]
            j = local_to_global[row_global]
            ax = axes[j, i]

            xmin, xmax = ax.get_xlim()
            ymin, ymax = ax.get_ylim()

            xgrid = np.linspace(xmin, xmax, ngrid)
            ygrid = np.linspace(ymin, ymax, ngrid)
            X, Y = np.meshgrid(xgrid, ygrid, indexing="xy")

            same_pair = (col_global // 2) == (row_global // 2)

            # True paired Rosenbrock block
            if same_pair and abs(col_global - row_global) == 1:
                p2 = rosenbrock_2d_pdf(X, Y, sigma=sigma, a=a, b=b)

                levels = np.max(p2) * np.array([0.20, 0.45, 0.70, 0.95])
                levels = np.asarray(levels, dtype=float)
                levels = levels[np.isfinite(levels)]
                levels = np.unique(np.sort(levels))

                if len(levels) >= 2:
                    ax.contour(
                        X, Y, p2,
                        levels=levels,
                        colors=color,
                        linewidths=1.8,
                        linestyles="dashed",
                        alpha=0.5,
                    )

            # Cross-pair panels: independent product of 1D marginals
            else:
                if col_global not in one_d or row_global not in one_d:
                    continue

                px_interp = np.interp(
                    xgrid,
                    one_d[col_global]["grid"],
                    one_d[col_global]["pdf"],
                    left=0.0,
                    right=0.0,
                )
                py_interp = np.interp(
                    ygrid,
                    one_d[row_global]["grid"],
                    one_d[row_global]["pdf"],
                    left=0.0,
                    right=0.0,
                )

                p_indep = py_interp[:, None] * px_interp[None, :]

                levels = np.max(p_indep) * np.array([0.05, 0.12, 0.45, 0.70, 0.90])
                levels = np.asarray(levels, dtype=float)
                levels = levels[np.isfinite(levels)]
                levels = np.unique(np.sort(levels))

                if len(levels) >= 2:
                    ax.contour(
                        X, Y, p_indep,
                        levels=levels,
                        colors=color,
                        linewidths=1.8,
                        linestyles="dashed",
                        alpha=0.5,
                    )

    return None
    
def gaussian_1d_marginal(xgrid:np.ndarray, mu:float, var:float) -> np.ndarray:
    """
    1d Gaussian marginal distribution for corner plot overlay.

    Arguments:
    ----------
    xgrid:
        Grid of x values to solve the gaussian over.
    mu:
        Centroid of gaussian distribution.
    var:
        Variance of the gaussian distribution.
        
    """
    return norm.pdf(xgrid, loc=mu, scale = np.sqrt(var))

def gaussian_2d_marginal(X:np.ndarray, Y:np.ndarray, mu2:np.ndarray, cov2:np.ndarray) -> np.ndarray:
    """
    2d gaussian marginal for 2 separate dimensions for corner plot overlay.

    Arguments:
    ----------
    X, Y:
        np.meshgrid outputs. The grids in X and Y over which this multivariate gaussian is evaluated.
    mu2:
        Centroids of each dimension's gaussian.
    cov2:
        Covariance matrix of the multivariate gaussian. 
    """
    pos = np.dstack((X, Y)) #Pass X and Y into a set of ordered (x, y) pairs to evaluate over.
    return multivariate_normal(mean=mu2, cov=cov2).pdf(pos)

def overlay_helper_gaussian(fig, dim_samples, dims, mu, cov, color = 'crimson', ngrid = 250):
    
    k = len(dims)
    axes = np.array(fig.axes).reshape((k,k))
    local_to_global = {g:i for i, g in enumerate(dims)} #dictionary to associate global dimension index w/ local indices of corner plot
    
    for col_global in dims: #iterate over all the columns
        for row_global in dims: #iterate over all the rows
            if row_global < col_global: #skip the upper triangular charts (which are empty), but INCLUDE the diagonals here
                continue

            i = local_to_global[col_global] #identify local index from global parameter index
            j = local_to_global[row_global]

            ax = axes[j, i]

            if j == i: #diagonal, only need one dimension
                x = dim_samples[:, i] #grab all samples corresponding to this diagonal
                
                xspan = np.ptp(x) #determine dynamic range of x,
                xpad = 0.35 * xspan #add some padding for chart cleanliness

                if xspan <= 0: #dont add anything if the dimension has no spatial extent
                    continue
                
                xgrid = np.linspace(x.min() - xpad, x.max() + xpad, ngrid) #generate grid of points to evaluate the gaussian at

                overlay = gaussian_1d_marginal(xgrid, mu=mu[col_global], var = cov[col_global,row_global]) #generate expected gaussian using true mean and precision matrix at [i,i]
                ymin, ymax = ax.get_ylim() #adding axis scaling to fit gaussian to normalized histogram made by corner.corner. First take max
                target_height = 0.85 * ymax if ymax > 0 else 1.0 #determine the maximum target height
                yplot = overlay / np.max(overlay) * target_height #scale the overlay to this max height
                ax.plot(xgrid, yplot, color = color, lw = 1.8, alpha = 0.8, zorder = 10) #plot with new normalized gaussian.
            
            else:
                x = dim_samples[:, i] #grab parameter along x axis samples
                y = dim_samples[:, j] #grab parameter along y axis samples
                
                #take their dynamic ranges
                xspan, yspan = np.ptp(x), np.ptp(y)

                #add padding
                xpad = 0.35 * xspan
                ypad = 0.35 * yspan

                if xspan <= 0 or yspan <= 0: #dont add anything if either dimension has no spatial extent
                    continue

                xgrid = np.linspace(x.min() - xpad, x.max() + xpad, ngrid) #generate grids to evaluate multivariate gaussian over
                ygrid = np.linspace(y.min() - ypad, y.max() + ypad, ngrid)

                X, Y = np.meshgrid(xgrid, ygrid, indexing = 'xy') #construct meshgrid
                P = gaussian_2d_marginal(X, Y, mu2 = mu[[col_global,row_global]], cov2 = cov[np.ix_([col_global,row_global],[col_global,row_global])])

                levels = np.max(P) * np.array([0.20, 0.45, 0.70, 0.95])
                levels = np.asarray(levels, dtype=float)
                levels = levels[np.isfinite(levels)]
                levels = np.unique(np.sort(levels))

                if len(levels) >= 2:
                    ax.contour(X, Y, P, levels=levels,
                            colors=color, linewidths=1.8,
                            linestyles='dashed', alpha=0.5)
                else:
                    print(f"Skipping contour for dims ({row_global}, {col_global}): invalid levels {levels}")

def transform_helper(points: np.ndarray, transform:dict | None = None):
    """
    Undoes the applied affine transformation to keep marginals consistent with plotted data. 
    """
    points = np.asarray(points)

    if transform is None or not transform.get('affine', False):
        return points

    A = np.asarray(transform['A'])
    B = np.asarray(transform['B'])
    Ainv = np.linalg.inv(A)

    orig_shape = points.shape
    pts2 = points.reshape(-1, orig_shape[-1])
    out = (pts2 - B) @ Ainv.T
    return out.reshape(orig_shape)












