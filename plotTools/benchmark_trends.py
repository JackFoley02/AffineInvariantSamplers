#imports
import matplotlib.pyplot as plt
import pandas as pd
import numpy as np
import os
from matplotlib import rc
rc('text', usetex=True)

#defining functions:
def normalize(series: np.ndarray) -> np.ndarray:
    """
    Ensures the input series has shape (W, T, D) for W walkers, T steps, and D dimensions.
    Prevents errors in corner package when single walker samplers (e.g. HMC) return arrays of (T, D)
    """
    series_arr = np.asarray(series)
    if series_arr.ndim == 2: #check for (T, D) shaped array
        series_arr = series_arr[None, :, :] #reshaping array (T, D) to (W, T, D) w/ W = 1
    #now that the array has been reshaped, let's check its size:
    if series_arr.ndim !=3:
        raise ValueError(f'Invalid series shape: should be (T, D) or (W, T, D), instead got {series_arr.shape}')
    return series_arr

def plot_trends(samples: np.ndarray, burnIn: np.ndarray, sig:float = None, outfile: str = None, labels: list[str] = None, title: str = None):
    """
    Generates trend plots for the supplied samples, using the given parameter dimensions.
    Plots are generated using a fed title and list of labels. Plotes are saved at the given directory
    
    Arguments:
    ----------
    samples:
        MCMC samples generated by a sampling algorithm
    labels:
        list of labels for each parameter
    title:
        title for the generated plot
    outfile:
        director path for the generated plot
    """
    #first, make sure the directory is correctly specified:
    if outfile is None:
        raise FileNotFoundError('Improper definition of output directory. Make sure it is defined properly!')

    W, T, D = samples.shape #determining shape of the data

    nrows = min((D+1) // 2 , 5)#defining dynamic number of rows
    fig, axs = plt.subplots(nrows = nrows, ncols = 2, figsize = (12, 3*nrows), sharex = True, constrained_layout = True) #Defined in terms of the dimensionality, to make space for additional dimensions
    axs = axs.flatten()

    #Defining sequence of pre- and post-burn-in samples for plotting
    full = np.concatenate([burnIn, samples], axis = 1)


    if labels is None: #define placeholders if no labels are given
        labels = [rf'$x_{{{i}}}$' for i in range(D)]

    for idx, ax in enumerate(axs): #iterate over the plot grid to make the plots
        if idx >= D:
            ax.set_visible(False)
            continue
        if idx >= 10:
            break

        ax.plot(full[:2, :, idx].T, alpha = 0.7, zorder = 4) #plot first 3 walkers for the indexed variable
        ax.plot(full[:, :, idx].T, alpha = 0.2, color = 'lightgray', zorder = 1) #add all walkers in the background
        ax.axvline(x=burnIn.shape[1], zorder = 5, color = 'black', linestyle = '--') #adding cutoff line to separate burn-in from the rest of the samples
        ax.set_ylabel(labels[idx])

        if (idx == (D - 1)) or (idx == (D - 2)):
            ax.set_xlabel('Step Number')

    plt.savefig(outfile, dpi = 200, bbox_inches = 'tight')
    plt.close()

def benchmark_trends(results: dict, outdir:str, distrib:str = ''):
    """
    Wrapper function to generate plots for all different samplers

    Arguments:
    ----------
    results:
        results dictionary from benchmark functions
    outdir:
        output directory path
    """

    os.makedirs(outdir, exist_ok = True) #make output directory if it doesn't already exist

    for name, res in results.items(): #iterate over the different samplers

        series = res['series']
        series = normalize(series) #normalized for shape consistency
        burn_in = res['burn_in']
        burn_in = normalize(burn_in)

        sigma = res['sigma']

        if "labels" not in res:
            labelList = None
        else:
            labelList = res['labels']

        safe_name = name.replace(" ", "_")
        outfile = os.path.join(outdir, f'trends_{safe_name}.pdf')

        plot_trends(series, burnIn = burn_in, sig = sigma, outfile = outfile, labels = labelList, title = distrib)
        print('Saved', outfile)




